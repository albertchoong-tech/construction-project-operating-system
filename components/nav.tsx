export function Sidebar({
  role,
  fullName,
  email,
}: {
  role: Role;
  fullName: string;
  email: string;
}) {
  const pathname = usePathname();
  const items = NAV_ITEMS.filter((item) =>
    canAccessModule(role, item.href === "/" ? "" : item.href.slice(1)),
  );
  return (
    <aside className="fixed inset-y-0 left-0 w-60 bg-slate-900 text-slate-300 hidden lg:flex flex-col z-10">
      <div className="px-5 py-5 border-b border-slate-800">
        <Link href="/" className="block">
          <span className="text-lg font-bold text-white tracking-tight">HSH ProjectOS</span>
          <span className="block text-[11px] text-slate-400 mt-0.5">
            Construction Project Operating System
          </span>
        </Link>
      </div>
      <nav className="flex-1 overflow-y-auto py-3 px-2 space-y-0.5">
        {items.map((item) => {
          const active =
            item.href === "/" ? pathname === "/" : pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors ${
                active
                  ? "bg-slate-800 text-white"
                  : "text-slate-400 hover:bg-slate-800/60 hover:text-slate-200"
              }`}
            >
              <svg className="w-[18px] h-[18px] shrink-0" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor">
                <path strokeLinecap="round" strokeLinejoin="round" d={item.icon} />
              </svg>
              {item.label}
            </Link>
          );
        })}
      </nav>
      <div className="px-4 py-4 border-t border-slate-800">
        <p className="text-sm font-medium text-slate-200 truncate">{fullName}</p>
        <p className="text-[11px] text-slate-500 truncate">
          {ROLE_LABELS[role]} · {email}
        </p>
        <button
          onClick={() => signOut()}
          className="mt-2.5 w-full rounded-lg border border-slate-700 px-3 py-1.5 text-xs font-medium text-slate-300 hover:bg-slate-800 hover:text-white transition-colors"
        >
          Sign out
        </button>
      </div>
    </aside>
  );
}
